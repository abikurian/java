import java.util.*;

public class naturalseries {
public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    System.out.println("Enter the value of n: ");
    int n = sc.nextInt();
    System.out.println("The natural numbers are: ");
    for (int i=1;i<=n;i++)
    {
        System.out.println(i);
    }
}    
}
